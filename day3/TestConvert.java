public class TestConvert{
	public static void main(String[] args){
		short s=123;
		int i=s;
		System.out.println(i);
		
		float f=3.5F;
		double d=f;
		System.out.println(d);
		
		
		byte b=10;
		double d2=b;
		
		System.out.println(d2);
		
		double d3=100;
		System.out.println(d3);
		
		float d4=(float)100.0;
		System.out.println(d4);
	}
}