public class TestFloatAndDouble{
	public static void main(String[] args){
		float f=1.2F;
		System.out.println(f);
		
		double d=1.3;
		System.out.println(d);
		
		System.out.println("--------------------------");
		
		double d2=2000;
		System.out.println(d2);
		
		double d3=2000D;
		System.out.println(d3);
		
		double d4=2E3;
		System.out.println(d4);
	}
}